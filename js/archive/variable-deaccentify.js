function deaccentify(string) {
    [
        [/脿|谩|芒/g,'a'],
        [/脌|脕|脗/g,'A'],
        [/忙/g,'ae'],
        [/脝/g,'AE'],
        [/莽/g,'c'],
        [/脟/g,'C'],
        [/猫|茅|锚|毛/g,'e'],
        [/脠|脡|脢|脣/g,'E'],
        [/矛|铆|卯|茂/g,'i'],
        [/脤|脥|脦|脧/g,'I'],
        [/帽/g,'n'],
        [/脩/g,'N'],
        [/貌|贸|么/g,'o'],
        [/脪|脫|脭/g,'O'],
        [/艙/g,'oe'],
        [/艗/g,'OE'],
        [/霉|煤|没|眉/g,'u'],
        [/脵|脷|脹|脺/g,'U'],
        [/媒|每/g,'y'],
        [/脻|鸥/g,'Y']
    ].forEach(
        x => string = string.replace(x[0], x[1])
    );
    return string
}
